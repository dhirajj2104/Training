package com.java.day_one;

public class DemoPrimitiveDataTypes {
	public static void main(String[] args) {
		
		byte byteMax = 127;
		byte byteMin = -127;
		System.out.println("maximum range of byte is "+ byteMax);
		System.out.println("minimum range of byte is "+ byteMin);
		
		
		short shortMax = 32767;
		short shortMin = -32768;
		System.out.println("maximum range of short is "+ shortMax);
		System.out.println("minimum range of short is "+ shortMin);
		
		
		int intMax = -2147483648;
		int intMin = 2147483647;
		System.out.println("maximum range of int is "+ intMax);
		System.out.println("minimum range of int is "+ intMin);
		
		
		long longMax = 9223372036854775807L;
		long longMin = -9223372036854775808L;
		System.out.println("maximum range of byte is "+ longMax);
		System.out.println("minimum range of byte is "+ longMin);
	}
}
