package com.java.day_one;

public class DemoOfDataTypes {
	public static void main(String[] args) {
		
		int val1 = 13/2;
		System.out.println(val1);
		
		float val2 = 356f / 97f;
		System.out.println(val2);
		
		double val3 = 356d / 97d;
		System.out.println(val3);
		
		int r = 16;
		double cirumference = 2*3.14*r;
		System.out.println(cirumference);
	}
}
